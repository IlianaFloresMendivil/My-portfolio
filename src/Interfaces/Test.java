    public static void Listar(DefaultTableModel modelo, String filtro) {
        try {
            Connection con = Conexion.getConnection();
            Statement sqlcon = con.createStatement();
            String Consulta = "SELECT id, nombre FROM dbo.Boutique.TipoArticulo";
            ResultSet resultado = sqlcon.executeQuery(Consulta);

            modelo.setRowCount(0);

            while (resultado.next()) {

                modelo.addRow(new Object[]{
                    resultado.getInt("id"),
                    resultado.getString("nombre"),
                    resultado.getString("Descripcion")
                });
            }
        } catch (SQLException ex) {
            System.out.println("Error: " + ex.toString());
        }
    }

