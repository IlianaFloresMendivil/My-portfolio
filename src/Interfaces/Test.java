    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = tblTabla.getSelectedRow();
        if (filaSeleccionada != -1) {
            int id = (int) tblTabla.getValueAt(filaSeleccionada, 0);

            // Ask for confirmation
            int confirmDialogResult = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar este elemento?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

            if (confirmDialogResult == JOptionPane.YES_OPTION) {
                ClaseTest.Eliminar(id);
                ClaseTest.Listar("");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un elemento para eliminar");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int filaSeleccionada = tblTabla.getSelectedRow();
        if (filaSeleccionada != -1) {
            int id = (int) tblTabla.getValueAt(filaSeleccionada, 0);
            String nuevoNombre = "perro";
            ClaseTest.Modificar(id, nuevoNombre);
            ClaseTest.Listar("");
        } else {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un elemento para editar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

